public class e1 {
    public static void main(String[] args) {
        String nombre = "Josué";
        int numHermanos = 2;
        boolean gustaJava = true;
        double promedioNotas = 15.3;

        numHermanos = numHermanos + 1;

        boolean esPromedioAlto = promedioNotas > 14.0;

        System.out.println("Hola, soy " + nombre + ", tengo " + numHermanos + " hermanos.");
        System.out.println("¿Me gusta Java? " + gustaJava);
        System.out.println("¿Mi promedio es mayor que 14.0? " + esPromedioAlto);
    }
}

