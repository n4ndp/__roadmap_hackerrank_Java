public class e2 {
    public static void main(String[] args) {
        final double PI = Math.PI;
        double radio = 5.0;
        double area = PI * radio * radio;
        
        System.out.println("Área: " + area);

        System.out.println("Clasificación: " + (area >= 100 ? "Grande" : "Pequeño"));
    }
}

