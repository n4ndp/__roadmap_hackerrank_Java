public class e6 {
    public static void main(String[] args) {
        int opcion = 3;

        switch (opcion) {
            case 1:
                System.out.println("Agregar item");
                break;
            case 2:
                System.out.println("Eliminar item");
                break;
            case 3:
                System.out.println("Actualizar item");
                break;
            case 4:
                System.out.println("Salir");
                break;
            default:
                System.out.println("Opción inválida");
        }
    }
}

